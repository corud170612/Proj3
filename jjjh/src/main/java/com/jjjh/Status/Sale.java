package com.jjjh.Status;

public class Sale {
   //상품번호
   private int no;
   //상품이름
   private String prodname;
   //상품가격
   private String price;
   //구독개월
   private String submon;
   //구매자 이름
   private String cid;
   //구매자 주소
   private String caddr;
   //원두 용량
   private String subvol;
   //사업자등록 번호
   private String id;
   //처리상태
   private String condition;
   //브랜드명
   private String storename;
   //분쇄유무
   private String grinding;
public int getNo() {
	return no;
}
public void setNo(int no) {
	this.no = no;
}
public String getProdname() {
	return prodname;
}
public void setProdname(String prodname) {
	this.prodname = prodname;
}
public String getPrice() {
	return price;
}
public void setPrice(String price) {
	this.price = price;
}
public String getSubmon() {
	return submon;
}
public void setSubmon(String submon) {
	this.submon = submon;
}
public String getCid() {
	return cid;
}
public void setCid(String cid) {
	this.cid = cid;
}
public String getCaddr() {
	return caddr;
}
public void setCaddr(String caddr) {
	this.caddr = caddr;
}
public String getSubvol() {
	return subvol;
}
public void setSubvol(String subvol) {
	this.subvol = subvol;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getCondition() {
	return condition;
}
public void setCondition(String condition) {
	this.condition = condition;
}
public String getStorename() {
	return storename;
}
public void setStorename(String storename) {
	this.storename = storename;
}
public String getGrinding() {
	return grinding;
}
public void setGrinding(String grinding) {
	this.grinding = grinding;
}


   
}